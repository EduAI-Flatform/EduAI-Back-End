const fs = require('node:fs');
const path = require('node:path');

const prismaDir = path.resolve(__dirname, '..', 'prisma');
const schemaPartsDir = path.join(prismaDir, 'schema');
const outputFile = path.join(prismaDir, 'schema.prisma');

if (!fs.existsSync(schemaPartsDir)) {
  throw new Error(`Prisma schema folder not found: ${schemaPartsDir}`);
}

const schemaFiles = fs
  .readdirSync(schemaPartsDir)
  .filter((fileName) => fileName.endsWith('.prisma'))
  .sort((left, right) => left.localeCompare(right));

if (schemaFiles.length === 0) {
  throw new Error(`No .prisma files found in: ${schemaPartsDir}`);
}

const sections = schemaFiles.map((fileName) => {
  const filePath = path.join(schemaPartsDir, fileName);
  return fs.readFileSync(filePath, 'utf8').trim();
});

const header = [
  '// This file is generated by scripts/merge-prisma-schema.js.',
  '// Edit files in prisma/schema/*.prisma instead.',
].join('\n');

const generatedSchema = `${header}\n\n${sections.join('\n\n')}\n`;

fs.writeFileSync(outputFile, generatedSchema);
process.stdout.write(`Merged ${schemaFiles.length} Prisma schema files into prisma/schema.prisma\n`);
